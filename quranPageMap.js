// ============================================================
// GET PAGE NUMBER FOR ANY VERSE
// ============================================================
function getPage(surah, verse) {
  let left = 0;
  let right = pageStart.length - 1;
  let result = 1;

  while (left <= right) {
    const mid = Math.floor((left + right) / 2);
    const boundary = pageStart[mid];

    if (boundary.surah < surah || (boundary.surah === surah && boundary.verse <= verse)) {
      result = boundary.page;
      left = mid + 1;
    } else {
      right = mid - 1;
    }
  }

  return result;
}

// ============================================================
// CALCULATE TOTAL PAGES FOR A RANGE
// ============================================================
function calculatePages(surah, startVerse, endVerse) {
  const startPage = getPage(surah, startVerse);
  const endPage = getPage(surah, endVerse);
  return endPage - startPage + 1;
}
